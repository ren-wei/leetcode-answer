const isValidSudoku = require('../36.有效的数独');

test('36.有效的数独', () => {
    expect(isValidSudoku(
        [
            ['.', '.', '.', '.', '5', '.', '.', '1', '.'],
            ['.', '4', '.', '3', '.', '.', '.', '.', '.'],
            ['.', '.', '.', '.', '.', '3', '.', '.', '1'],
            ['8', '.', '.', '.', '.', '.', '.', '2', '.'],
            ['.', '.', '2', '.', '7', '.', '.', '.', '.'],
            ['.', '1', '5', '.', '.', '.', '.', '.', '.'],
            ['.', '.', '.', '.', '.', '2', '.', '.', '.'],
            ['.', '2', '.', '9', '.', '.', '.', '.', '.'],
            ['.', '.', '4', '.', '.', '.', '.', '.', '.'],
        ]
    )).toBe(false);
});
